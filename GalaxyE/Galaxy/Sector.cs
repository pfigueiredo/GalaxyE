			ulong value0, value1, value2, value3;

			value0 = (ulong)TheMilkyWay[pixelval].R;
			value0 += TheMilkyWay[pixelval].G;
			value0 += (ulong)TheMilkyWay[pixelval].B;
			value0 /= 3;
			//value0 = value0 * 64 / 255;
			//value0 &= 63;

			value1 = (ulong)TheMilkyWay[pixelval + 128].R;
			value1 += TheMilkyWay[pixelval + 128].G;
			value1 += (ulong)TheMilkyWay[pixelval + 128].B;
			value1 /= 3;
			//value1 = value1 * 64 / 255;
			//value1 &= 63;

			value2 = (ulong)TheMilkyWay[pixelval + 128 + 1].R;
			value2 += TheMilkyWay[pixelval + 128 + 1].G;
			value2 += (ulong)TheMilkyWay[pixelval + 128 + 1].B;
			value2 /= 3;
			//value2 = value2 * 64 / 255;
			//value2 &= 63;

			value3 = (ulong)TheMilkyWay[pixelval + 1].R;
			value3 += TheMilkyWay[pixelval + 1].G;
			value3 += (ulong)TheMilkyWay[pixelval + 1].B;
			value3 /= 3;
			//value3 = value3 * 64 / 255;
			//value3 &= 63;

			p1 = (ulong)value0;     // Current center
			p3 = (ulong)value1;     // Next row
			p4 = (ulong)value2;     // Next row, next column
			p2 = (ulong)value3;     // Next column
			coordx = (coordx * 512) & 32256;
			coordy = (coordy * 512) & 32256;
			ebx = (p2 - p1) * coordx + (p3 - p1) * coordy;    // -0x0FB0400...0x0FB0400
			esi = (coordx * coordy) >> 15;        // 0..0x7C08
			edi = p4 - p3 - p2 + p1;
			esi *= edi;
			ebx += esi;
			ebx += ebx;
			p1 <<= 16;                // p1 is now VVVV.VVVV.0000.0000
			ebx += p1;
			ecx = ebx >> 8;
			if (galaxyScale < 16) {
				ebx = coordx + ecx;
				eax = coordx * coordy;
				eax = (ulong)(((long)eax) >> 15);
				ebx ^= eax;
				ebx = (ulong)(((long)ebx) >> 5);
				ebx &= 0x7f; //0x3f; //0xff; //7f;
				eax = SystemDensity[ebx];
				if (galaxyScale != 0) {
					edx = (ulong)(16 - galaxyScale);
					edx *= eax;
					edx = (ulong)(((long)edx) >> 5);
					eax = 0xffff - edx;
					ecx *= eax;
					ecx >>= 16;
				} else {
					ecx *= eax;
					ecx >>= 16;
				}
			}
			p1 = ecx;
			p1 >>= 10;

			r = ((int)p1 * r / 15) & 0xFF;
			g = ((int)p1 * g / 15) & 0xFF;
			b = ((int)p1 * b / 15) & 0xFF;

			//_sectorColor = Color.FromArgb(r, g, b);

			return (int)p1 >> 1;// &0x0F;
		}
