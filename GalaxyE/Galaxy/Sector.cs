		public ulong[] GetSystemParams(int sysNum) {
			uint coordx = this._indexX;
			uint coordy = this._indexY;
			int number_of_systems = this._numSystems;
			List<StarSystem> starsSystem = new List<StarSystem>();
			int i;

			ulong SystemParam_0 = (coordx << 16) + coordy;
			ulong SystemParam_1 = (coordy << 16) + coordx;

			Utils.Rotate_SystemParams(ref SystemParam_0, ref SystemParam_1);
			Utils.Rotate_SystemParams(ref SystemParam_0, ref SystemParam_1);
			Utils.Rotate_SystemParams(ref SystemParam_0, ref SystemParam_1);

			for (i = 0; i <= sysNum; i++) {
				Utils.Rotate_SystemParams(ref SystemParam_0, ref SystemParam_1);

				if (i == sysNum) {
					return new ulong[] { SystemParam_0, SystemParam_1 };
				}
			}

			return new ulong[] { 0, 0 };
		}


		private List<StarSystem> Generate_Systems() {
			uint coordx = this._indexX;
			uint coordy = this._indexY;
			int number_of_systems = this._numSystems;
			List<StarSystem> starsSystem = new List<StarSystem>();
			int i;

			ulong SystemParam_0 = (coordx << 16) + coordy;
			ulong SystemParam_1 = (coordy << 16) + coordx;

			Utils.Rotate_SystemParams(ref SystemParam_0, ref SystemParam_1);
			Utils.Rotate_SystemParams(ref SystemParam_0, ref SystemParam_1);
			Utils.Rotate_SystemParams(ref SystemParam_0, ref SystemParam_1);

			for (i = 0; i < number_of_systems; i++) {
				Utils.Rotate_SystemParams(ref SystemParam_0, ref SystemParam_1);

				starsSystem.Add(new StarSystem(this, i, SystemParam_0, SystemParam_1));


			}

			return starsSystem;
		}
