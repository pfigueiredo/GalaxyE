		protected internal bool BuildKnownSpace() {

			string sectorKey = string.Format("{0}.{1}", this._indexX, this._indexY);

			if (KnownSpace.Data.ContainsKey(sectorKey)) {
				foreach (var s in KnownSpace.Data[sectorKey]) {
					StarSystem system = new StarSystem(
						this, s.SysNum, s.TypeCode, s.Name,
						s.SX, s.SY, s.SZ
					);
					this.AddSystem(system);
				}

				return true;
			}

			return false;

