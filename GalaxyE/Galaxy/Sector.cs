			double sectorSize = GalaxyConstants.SectorWidth; //100000 / 8191;
			double difX = Math.Abs((int)this.IndexX - (int)coordX) * sectorSize;
			double difY = Math.Abs((int)this.IndexY - (int)coordY) * sectorSize;

			return Math.Sqrt(Math.Pow(difX, 2) + Math.Pow(difY, 2));

